-- STORAGE SETUP
-- Run these to enable storage for uploads
-- 1. Create the bucket (Safe to run multiple times)
INSERT INTO storage.buckets (id, name, public) 
VALUES ('EduAdmin', 'EduAdmin', true)
ON CONFLICT (id) DO NOTHING;

-- 2. Create policies for the bucket
-- Note: If you get "policy already exists" errors, you can ignore them or drop the policy first.

-- Policy to allow anyone to read public files
DO $$ 
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_policies 
        WHERE tablename = 'objects' 
        AND schemaname = 'storage' 
        AND policyname = 'Public Access'
    ) THEN
        CREATE POLICY "Public Access" ON storage.objects FOR SELECT USING (bucket_id = 'EduAdmin');
    END IF;
END $$;

-- Policy to allow authenticated uploads
DO $$ 
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_policies 
        WHERE tablename = 'objects' 
        AND schemaname = 'storage' 
        AND policyname = 'Allow Uploads'
    ) THEN
        CREATE POLICY "Allow Uploads" ON storage.objects FOR INSERT WITH CHECK (bucket_id = 'EduAdmin');
    END IF;
END $$;

-- Policy to allow updates
DO $$ 
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_policies 
        WHERE tablename = 'objects' 
        AND schemaname = 'storage' 
        AND policyname = 'Allow Updates'
    ) THEN
        CREATE POLICY "Allow Updates" ON storage.objects FOR UPDATE USING (bucket_id = 'EduAdmin');
    END IF;
END $$;

-- Policy to allow deletes
DO $$ 
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_policies 
        WHERE tablename = 'objects' 
        AND schemaname = 'storage' 
        AND policyname = 'Allow Deletes'
    ) THEN
        CREATE POLICY "Allow Deletes" ON storage.objects FOR DELETE USING (bucket_id = 'EduAdmin');
    END IF;
END $$;
