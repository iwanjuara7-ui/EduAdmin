-- Users Table
CREATE TABLE IF NOT EXISTS users (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    email TEXT UNIQUE NOT NULL,
    password TEXT NOT NULL,
    name TEXT NOT NULL,
    subject TEXT NOT NULL,
    photo_url TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Enable RLS on users
ALTER TABLE users ENABLE ROW LEVEL SECURITY;

-- Policies for users
-- Allow anyone to insert (for registration)
CREATE POLICY "Allow public registration" ON users FOR INSERT WITH CHECK (true);
-- Allow users to view their own profile
CREATE POLICY "Users can view own profile" ON users FOR SELECT USING (true); -- Simplified for prototype, ideally check ID
-- Allow users to update their own profile
CREATE POLICY "Users can update own profile" ON users FOR UPDATE USING (true); -- Simplified for prototype

-- Students (Siswa) Table
